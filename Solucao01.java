 /* A proposta é imprimir na tela todos os números de 100 a 1  a contagem vai ser regressiva.*/
import javax.swing.*;
public class Solucao01 {
    public static void main(String[] args) {

        for (int i = 100; i >= 0 ; i--) {
            JOptionPane.showMessageDialog(null,i);
        }

    }
  
}